                        Familienunternehmen wurde.</p>
                    <p>Aus gesundheitlichen Gründen ist der Firmengründer Reinhard Lücke 2006 aus unserem Unternehmen ausgeschieden.</p>
                    <p>Heute gehört der moderne Trocken- und Akustikbau mit allen seinen Facetten zum Hauptbetätigungsfeld der Firma Lücke.
                        Wir beschäftigen derzeit auf unseren Baustellen 20-25 gewerbliche Mitarbeiter und bei Bedarf diverse Subunternehmen, deren Arbeit
                        jedoch von unseren Fachkräften überwacht wird.</p>
                    <p>Für die Baustellenüberwachung sind unsere Projektleiter und Gesellschafter Jörn und Guido Lücke sowie unsere Diplom-Bauingenieure
                        Markus Kießling und Heiko Brandes zuständig.</p>
                    <p>Alle Projektleiter betreuen Sie von der Angebotserarbeitung bis zur Endabnahme. Bei der Abrechnung der Baustellen werden unsere
                        Projektleiter von Jutta Fischer tatkräftig unterstützt. Die Buchhaltung übernimmt bei uns Mandy Rademacher, die Ihnen auch gerne bei
