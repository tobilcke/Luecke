                    <p>Unser Unternehmen wurde im Jahr 1991 durch die Partner Reinhard Lücke, Jörn Lücke und Jörg Friedrich in Lengede gegründet.</p>
                    <p>In den ersten Jahren waren wir hauptsächlich im Eigenheimbau tätig. So beschränkte sich unser Aufgabenfeld zu diesem Zeitpunkt auf
                        die Ausführung kleinerer Dachgeschossausbauten.</p>
                    <p>1992 erfolgte der Firmenumzug von Lengede nach Meerdorf und damit die Trennung von unserem Teilhaber Jörg Friedrich. Verstärkung
